    carp=sayi1*sayi2;
    cikar=sayi1-sayi2;
    bol=sayi1/sayi2;
    
    printf("girilen sayilarin toplami= %d",topla);
    printf("\ngirilen sayilarin carpimi= %d",carp);
    printf("\ngirilen sayilarin cikarmasi= %d",cikar);
    printf("\ngirilen sayilarin bolumu= %d",bol);
    
